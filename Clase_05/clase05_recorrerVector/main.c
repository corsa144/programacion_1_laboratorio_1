#include <stdio.h>
#include <stdlib.h>
#define CANT 100

int main()
{
    int max,min,i,datos[i];
    for(i=0;i<CANT;i++){

        if(i==0||datos[i]>max){
            max=datos[i];
        }
        if(i==0||datos[i]<min){
            min=datos[i];
        }
    }
    return 0;
}
